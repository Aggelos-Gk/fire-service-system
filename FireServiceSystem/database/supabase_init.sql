create table if not exists users (
  id bigint generated by default as identity primary key,
  username varchar(50) not null unique,
  first_name varchar(100),
  last_name varchar(100),
  telephone varchar(50),
  password varchar(255) not null,
  user_type varchar(20) not null default 'USER',
  birthdate date,
  gender varchar(10),
  country varchar(100),
  municipality varchar(100),
  address varchar(255),
  job varchar(100),
  volunteer_role varchar(20),
  lat double precision,
  lon double precision,
  created_at timestamp default now(),
  updated_at timestamp default now(),
  constraint chk_users_user_type check (user_type in ('ADMIN','USER','VOLUNTEER')),
  constraint chk_users_volunteer_role check (volunteer_role is null or volunteer_role in ('FIREFIGHTER','DRIVER'))
);

create table if not exists incidents (
  id bigint generated by default as identity primary key,
  incident_code varchar(50) not null unique,
  title varchar(255) not null,
  description text,
  status varchar(30) not null default 'active',
  priority varchar(20) not null default 'medium',
  location varchar(255),
  lat double precision,
  lon double precision,
  needed_drivers int not null default 0,
  needed_firefighters int not null default 0,
  created_by bigint,
  created_at timestamp default now(),
  updated_at timestamp default now(),
  constraint fk_incidents_created_by foreign key (created_by) references users(id)
);

create table if not exists participants (
  id bigint generated by default as identity primary key,
  incident_id bigint not null,
  user_id bigint not null,
  role varchar(100),
  joined_at timestamp default now(),
  constraint fk_participants_incident foreign key (incident_id) references incidents(id),
  constraint fk_participants_user foreign key (user_id) references users(id),
  constraint chk_participants_role check (upper(role) in ('FIREFIGHTER','DRIVER'))
);

create table if not exists messages (
  id bigint generated by default as identity primary key,
  incident_id bigint,
  sender_id bigint,
  receiver_id bigint,
  title varchar(255) not null,
  content text not null,
  message_type varchar(30) not null default 'public',
  priority varchar(20) not null default 'normal',
  created_at timestamp default now(),
  constraint fk_messages_incident foreign key (incident_id) references incidents(id),
  constraint fk_messages_sender foreign key (sender_id) references users(id),
  constraint fk_messages_receiver foreign key (receiver_id) references users(id),
  constraint chk_messages_type check (message_type in ('public','private'))
);

create table if not exists participant_requests (
  id bigint generated by default as identity primary key,
  incident_id bigint not null,
  user_id bigint not null,
  requested_role varchar(100) not null,
  status varchar(30) not null default 'requested',
  created_at timestamp default now(),
  decided_at timestamp,
  decided_by bigint,
  constraint fk_participant_requests_incident foreign key (incident_id) references incidents(id),
  constraint fk_participant_requests_user foreign key (user_id) references users(id),
  constraint fk_participant_requests_decider foreign key (decided_by) references users(id),
  constraint chk_participant_requests_role check (upper(requested_role) in ('FIREFIGHTER','DRIVER'))
);

create table if not exists notification_reads (
  id bigint generated by default as identity primary key,
  user_id bigint not null,
  notification_id varchar(120) not null,
  read_at timestamp default now(),
  constraint fk_notification_reads_user foreign key (user_id) references users(id),
  constraint uq_notification_reads_user_notification unique (user_id, notification_id)
);


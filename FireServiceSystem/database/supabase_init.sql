-- -------------------------------------------------------------------
-- Seed notification reads (>= 25)
-- -------------------------------------------------------------------
with u as (
  select id, username
  from users
),
g as (
  select generate_series(1, 3) as n
)
insert into notification_reads (user_id, notification_id, read_at)
select
  u.id,
  'notif-' || u.username || '-' || g.n::text,
  now() - ((u.id % 10 + g.n) * interval '3 hours')
from u
cross join g
where not exists (
  select 1
  from notification_reads nr
  where nr.user_id = u.id and nr.notification_id = ('notif-' || u.username || '-' || g.n::text)
);

-- -------------------------------------------------------------------
-- Keep participation roles healthy
-- -------------------------------------------------------------------
update participants

  when upper(role) = 'DRIVER' then 'Driver'
  when upper(role) = 'FIREFIGHTER' then 'Firefighter'
  else role
end
where role is not null;

delete from participants
where role is null or upper(role) not in ('FIREFIGHTER','DRIVER');

update participant_requests
set requested_role = upper(requested_role)
where requested_role is not null;

delete from participant_requests
where requested_role is null or upper(requested_role) not in ('FIREFIGHTER','DRIVER');

